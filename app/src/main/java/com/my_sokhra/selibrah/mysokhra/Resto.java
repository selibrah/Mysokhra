package com.my_sokhra.selibrah.mysokhra;

public class Resto {

    String imgurl;
    String name;

    public Resto(String imgurl, String name) {
        this.imgurl = imgurl;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Resto() {
    }


    public String getImgurl() {
        return imgurl;
    }

    public void setImgurl(String imgurl) {
        this.imgurl = imgurl;
    }






}
